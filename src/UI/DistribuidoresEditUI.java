    public class buscarPlantaListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {

            d = (Distribuidor) distjC.getSelectedItem();
            long nit = d.getNit();
            String nombre = d.getNombre();
            String ciudad = d.getCiudad();
            long tel = d.getTelefono();
            String email = d.getEmail();
            String dir = d.getDireccion();

            nitjT.setText(Long.toString(nit));
            nomjT.setText(nombre);
            ciudadjT.setText(ciudad);
            emailjT.setText(email);
            teljT.setText(Long.toString(tel));
            dirjT.setText(dir);

        }

    }

    public class EditDistribuidorListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            try {
                //String cod = codPjT.getText();
                Long nit = Long.parseLong(nitjT.getText());
                String nom = nomjT.getText();
                String ciudad = ciudadjT.getText();
                String email = emailjT.getText();
                Long tel = Long.parseLong(teljT.getText());
                String dir = dirjT.getText();

                d = (Distribuidor) distjC.getSelectedItem();

                d.setNit(nit);
                d.setNombre(nom);
                d.setEmail(email);
                d.setDireccion(dir);
                d.setTelefono(tel);
                d.setCiudad(ciudad);

                controlDistribuidores.actualizarDistribuidor(d);

                JOptionPane.showMessageDialog(rootPane, "Distribuidor editado con éxito");
                canbtn.doClick();
            } catch (Exception ex) {
                Logger.getLogger(DistribuidoresEditUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }

    public class borrarListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            nitjT.setText("");
            nomjT.setText("");
            ciudadjT.setText("");
            emailjT.setText("");
            teljT.setText("");
            dirjT.setText("");

        }

    }
