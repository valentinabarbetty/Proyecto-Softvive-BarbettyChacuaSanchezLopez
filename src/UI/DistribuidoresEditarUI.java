    public class buscarDistribuidorListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {

            d = (Distribuidor) distjC.getSelectedItem();
            long nit = d.getNit();
            String nombre = d.getNombre();
            String ciudad = d.getCiudad();
            long tel = d.getTelefono();
            String email = d.getEmail();
            String dir = d.getDireccion();

            ciudadcbx.setSelectedItem(ciudad);
            ciudadcbx.updateUI();
            nitjT.setText(Long.toString(nit));
            nomjT.setText(nombre);
            ciudadjT.setText(ciudad);
            emailjT.setText(email);
            teljT.setText(Long.toString(tel));
            dirjT.setText(dir);

        }

    }

    public class EditarDistribuidorListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            if (nitjT.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar un NIT");
            }
            if (nomjT.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar un nombre");
            }
            if (teljT.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar un teléfono");
            }
            if (emailjT.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar un E-mail");
            }
            if (dirjT.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar una dirección");
            }
            if (ciudadcbx.getSelectedItem().equals(null)) {
                JOptionPane.showMessageDialog(rootPane, "Debe seleccionar una ciudad");
            }
            try {
                //String cod = codPjT.getText();
                Long nit = Long.parseLong(nitjT.getText());
                String nom = nomjT.getText();
                String ciudad = (String) ciudadcbx.getSelectedItem();
                String email = emailjT.getText();
                Long tel = Long.parseLong(teljT.getText());
                String dir = dirjT.getText();

                d = (Distribuidor) distjC.getSelectedItem();

                d.setNit(nit);
                d.setNombre(nom);
                d.setEmail(email);
                d.setDireccion(dir);
                d.setTelefono(tel);
                d.setCiudad(ciudad);

                controlDistribuidores.actualizarDistribuidor(d);

                JOptionPane.showMessageDialog(rootPane, "Distribuidor editado con éxito");
                canbtn.doClick();
            } catch (Exception ex) {
                Logger.getLogger(DistribuidoresEditarUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }

