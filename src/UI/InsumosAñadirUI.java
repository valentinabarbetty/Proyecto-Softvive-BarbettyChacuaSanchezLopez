    public class agregarInsumosListener implements ActionListener {

        @Override
        public void actionPerformed(ActionEvent e) {
            if (descripcionjT.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar la descripci칩n del insumo");
            } else if (nomjT.getText().isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Debe ingresar el nombre del insumo");
            } else {
                try {
                    String nombre = nomjT.getText();
                    String descripcion = descripcionjT.getText();

                    Insumo insumo = new Insumo(nombre, descripcion);

                    controlInsumos.AgregarInsumo(insumo);

                    JOptionPane.showMessageDialog(rootPane, "Insumo agregado con 칠xito");
                    cancelarbtn.doClick();
                } catch (Exception ex) {
//                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                }
            }
        }

    }

